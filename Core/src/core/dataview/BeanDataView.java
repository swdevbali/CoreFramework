    private void switchToolbar(int toolbarMode) {
        this.toolbarMode = toolbarMode;
        Component[] cmps = tbarUtama.getComponents();

        switch (toolbarMode) {
            case TOOLBAR_OK_CANCEL:
                btnTambah.setText("Simpan");
                btnTambah.setMnemonic('s');
                btnUbah.setText("Batal");
                btnTambah.setMnemonic('b');

                for (int i = 0; i < cmps.length; i++) {
                    cmps[i].setVisible(false);
                }
                btnUbah.setVisible(true);
                btnTambah.setVisible(true);
                break;
            case TOOLBAR_BROWSE:
                btnTambah.setText("Tambah");
                btnTambah.setMnemonic('t');
                btnUbah.setText("Ubah");
                btnTambah.setMnemonic('u');
                for (int i = 0; i < cmps.length; i++) {
                    cmps[i].setVisible(true);
                }
                break;
        }
    }

