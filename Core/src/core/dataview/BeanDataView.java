    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if (toolbarMode == TOOLBAR_BROWSE) {
            dataController.performTambahEx();            
            switchToolbar(TOOLBAR_OK_CANCEL);
        }else if (toolbarMode == TOOLBAR_OK_CANCEL){
            dataController.performOK();
            switchToolbar(TOOLBAR_BROWSE);
        }
        
    }//GEN-LAST:event_btnTambahActionPerformed

