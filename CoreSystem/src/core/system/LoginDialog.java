    private void cboNamaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboNamaActionPerformed
        if (cboNama.getSelectedIndex() >= 0) {
            LoggingWindow.getInstance().addToLog("User Selected");
            String detail[][] = DB.getInstance().getDataSet("select namaLengkap,keterangan,password from pemakai where nama='" + cboNama.getItemAt(cboNama.getSelectedIndex()) + "'", false);
            txtNamaLengkap.setText(detail[0][0]);
            lblKeterangan.setText(detail[0][1]);
            txtPassword.requestFocus();
            if(Startup.BYPASS_PASSWORD) txtPassword.setText(detail[0][2]);
        }
    }//GEN-LAST:event_cboNamaActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (PrivilegeDialog.getInstance().isCorrect()) {
            LoggingWindow.getInstance().addToLog("Edit Profile");
            InputPemakai.getInstance().setPemakai(Integer.parseInt(""+controllerPemakai.getAutoIdFromSelectedIndex()));
            InputPemakai.getInstance().setVisible(true);
            setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void doClose(int retStatus) {
        if (retStatus != RET_CANCEL) {
            returnStatus = retStatus;
            setVisible(false);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Anda harus login sebelum menggunakan program ini", "Login", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    @Override
    public void setVisible(boolean b) {
        if (b) {
            controllerPemakai.reload();
        } else {
            txtPassword.setText("");
        }
        super.setVisible(b);
    }
