    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        if (Startup.getInstance().authenticate(cboNama.getItemAt(
                cboNama.getSelectedIndex()) + 
                "", txtPassword.getPassword())) {
            LoggingWindow.getInstance().addToLog("Login Success");
            Authentication.getInstance().getUserInfo().setLogin(true);
            Authentication.getInstance().getListener().userLogin();
            doClose(RET_OK);
        } else {
            LoggingWindow.getInstance().addToLog("Login Failed");
            JOptionPane.showMessageDialog(this, 
                    "Maaf, password Anda tidak sesuai. Ulan gi lagi",
                    "Password salah", JOptionPane.ERROR_MESSAGE);
            txtPassword.requestFocus();
        }
    }//GEN-LAST:event_okButtonActionPerformed

