    public void setPemakai(int id) {
        this.id = id;
        if (id != -1) {
            String data[][] = DB.getInstance().getDataSet("select nama,namalengkap,password,keterangan,peran from pemakai where idpemakai=" + id, false);
            txtNama.setText(data[0][0]);
            txtNamaLengkap.setText(data[0][1]);
            txtPassword.setText(data[0][2]);
            txtKeterangan.setText(data[0][3]);
            if (data[0][4].equals("Operator")) {
                cboHakAkses.setSelectedIndex(0);
            } else if (data[0][4].equals("Administrator")) {
                cboHakAkses.setSelectedIndex(1);
            }
        } else {
            txtNama.setText("");
            txtNamaLengkap.setText("");
            txtPassword.setText("");
            txtKeterangan.setText("");
            cboHakAkses.setSelectedIndex(0);
        }
        txtNama.requestFocus();
    }

