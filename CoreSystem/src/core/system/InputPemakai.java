    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed

        if (id == -1) {
            DB.getInstance().executeQuery("insert into pemakai(nama,namalengkap,password,keterangan,peran) values('" + txtNama.getText() + "','" + txtNamaLengkap.getText() + "','" + txtPassword.getText() + "','" + txtKeterangan.getText() + "','" + cboHakAkses.getItemAt(cboHakAkses.getSelectedIndex()) + "')");
            doClose(RET_OK);
        } else {
            if (validateAllFields()) {
                DB.getInstance().executeQuery("update pemakai set " +
                        "nama='" + txtNama.getText() + "'," +
                        "namalengkap='" + txtNamaLengkap.getText() + "'," +
                        "password='" + txtPassword.getText() + "'," +
                        "peran='" + cboHakAkses.getItemAt(cboHakAkses.getSelectedIndex()) + "'," +
                        "keterangan=' " + txtKeterangan.getText() + "' " +
                        "where idpemakai = " + id);
                txtNama.setText("");
                doClose(RET_OK);
            } else {
                JOptionPane.showMessageDialog(null, "Lengkapi terlebih dahulu form input pemakai tersebut");
            }
        }

    }//GEN-LAST:event_okButtonActionPerformed

